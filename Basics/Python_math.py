# Python math functions. python have some built in math functions.
import math

# using min and max functions, which will return the minimum and maximum value from the given arguments.
x = min(1, 2, 3, 4)
y = max(1, 2, 3, 4)
print("Minimum value is:", x)
print("Maximum value is:", y)
# using abs function, which will return the absolute value of the given arguments
z = abs(-10)
print("\n")
print("Absolute value is:", z)
# the pow function, pow(x,y) will return the value of x raised to the power of y.
a = pow(2, 3)
print("\n")
print("2 raised to the power of 3 is:", a)
# in math module, we have a function called sqrt, which will return the square root of the given number.
a = math.sqrt(25)
print("\n")
print("Square root of 25 is:", a)
# math module's upward and downward functions, which will return the next nearest integer value.
b = math.ceil(2.3)
c = math.floor(2.3)
print("\n")
print("Ceiling value of 2.3 is:", b)
print("Floor value of 2.3 is:", c)
# math pi constant, which is the value of pi.
print("\n")
print("Value of pi is:", math.pi)
# math e constant, which is the value of e.
print("\n")
print("Value of e is:", math.e)
# math factorial function, which will return the factorial of the given number
print("\n")
print("Factorial of 5 is:", math.factorial(5))
# math gcd function, which will return the greatest common divisor of the given number
print("\n")
print("GCD of 12 and 15 is:", math.gcd(12, 15))
# math radius funtion, which will return the radius of the circle with the given area
print("\n")
print("Radiua of the circle with area 50 is:", math.sqrt(50 / math.pi))
# math log function, which will return the logarithm of the given number with the given base
print("\n")
print("logarothm of 100 with base 10 is:", math.log(100, 10))
# math log10 function, which will return the logarithm of the given number with base 10
print("\n")
print("Logarithm of 100 with base 10 is:", math.log10(100))
