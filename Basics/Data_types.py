# Setting the data type
# string data type
x = "Hello World"
print(type(x))
# integer data type
x = 36
print(type(x))
# float data type
x = 3.14
print(type(x))
#  complex data type
x = 1j
print(type(x))
# list data type
x = ["apple", "banana", "cherry"]
print(type(x))
# tuple data type
x = ("apple", "banana", "cherry")
print(type(x))
# range data type
x = range(6)
print(type(x))
# dict data type
x = {"name" : "john", "age" : 36}
print(type(x))
# set data type
x = {"apple", "banana", "cherry"}
print(type(x))
# frozenset data type
x = frozenset({"apple", "banana", "cherry"})
print(type(x))
# bool data type
x = True
print(type(x))
# bytes data type
x = b"Hello"
print(type(x))
# bytearray data type
x = bytearray(5)
print(type(x))
# memoryview data type
x = memoryview(bytes(5))
print(type(x))
# NoneType data type
x = None
print(type(x))
